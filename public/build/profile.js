function on() {
    document.getElementById("modp").style.display = "block";
}

function off() {
    document.getElementById("modp").style.display = "none";
}

/* Open */
function openNav() {
    document.getElementById("myNav").style.height = "100%";
}

/* Close */
function closeNav() {
    document.getElementById("myNav").style.height = "0%";
}